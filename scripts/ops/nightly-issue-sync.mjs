/**
 * Nightly Issue Sync — Phase C-2 (Auto-Issue)
 * 
 * 分類結果 (classification-YYYY-MM-DD.json) を読み取り、
 * 重要な改善項目を GitHub Issue に自動登録します。
 */

import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';

const ROOT = process.cwd();
const REPORT_DIR = path.join(ROOT, 'docs', 'nightly-patrol');

// --- Helpers ---

function getLatestClassificationJson() {
  if (!fs.existsSync(REPORT_DIR)) return null;
  const files = fs.readdirSync(REPORT_DIR)
    .filter(f => f.startsWith('classification-') && f.endsWith('.json'))
    .sort()
    .reverse();
  if (files.length === 0) return null;
  try {
    return JSON.parse(fs.readFileSync(path.join(REPORT_DIR, files[0]), 'utf8'));
  } catch {
    return null;
  }
}

function isCore(file) {
  const corePaths = [
    'src/features/daily/',
    'src/features/attendance/',
    'src/sharepoint/fields/',
    'src/sharepoint/spListRegistry.ts',
    'src/lib/sp/',
    'src/domain/',
  ];
  return corePaths.some(p => file.startsWith(p));
}

function getExistingIssues() {
  try {
    const output = execSync('gh issue list --label nightly-patrol --state open --json title,number', { stdio: 'pipe' }).toString();
    return JSON.parse(output);
  } catch {
    console.warn('⚠️ Failed to fetch existing issues via gh CLI.');
    return [];
  }
}

// --- Main ---

async function main() {
  const isDryRun = process.env.DRY_RUN === 'true';
  const classification = getLatestClassificationJson();
  if (!classification) {
    console.log('ℹ️ No classification found for today.');
    return;
  }

  const existingIssues = getExistingIssues();
  const issueActions = classification.classifications.filter(c => c.action === 'draft-issue');

  if (issueActions.length === 0) {
    console.log('✅ No new issues to create based on classification.');
    return;
  }

  console.log(`📋 Found ${issueActions.length} candidates for GitHub Issues.${isDryRun ? ' [DRY RUN]' : ''}`);

  for (const item of issueActions) {
    const isCoreIssue = item.affectedFiles.some(isCore);
    const category = isCoreIssue ? 'Core' : 'Perimeter';
    const title = `[Nightly Patrol] ${item.kind} (${category}) - ${item.severity}`;
    
    // 重複チェック
    if (existingIssues.some(i => i.title === title)) {
      console.log(`  ⏭️ Skip: Already exists - "${title}"`);
      continue;
    }

    const body = `## ${item.kind} Report (${classification.date})
- **Severity**: ${item.severity}
- **Category**: ${category}
- **Affected Files**: ${item.affectedFiles.length}

### Details
${item.affectedFiles.slice(0, 20).map(f => `- \`${f}\``).join('\n')}
${item.affectedFiles.length > 20 ? '\n...and more.' : ''}

---
*Generated by Nightly Issue Sync (Phase C-2) — [AI開発OS Phase 8 Guardrails](../../.agents/workflows/nightly.md)*`;

    const labels = [
      'nightly-patrol',
      `priority:${item.severity}`,
      `category:${category.toLowerCase()}`,
      item.kind
    ];

    if (isDryRun) {
      console.log(`  🔍 [DRY RUN] Would create issue: "${title}"`);
      console.log(`     Labels: ${labels.join(', ')}`);
      continue;
    }

    console.log(`  🚀 Creating issue: "${title}"...`);
    try {
      const labelsArg = labels.map(l => `"${l}"`).join(',');
      execSync(`gh issue create --title "${title}" --body "${body}" --label ${labelsArg}`, { stdio: 'pipe' });
      console.log('    ✅ Success.');
    } catch (e) {
      console.error(`    ❌ Failed to create issue for ${item.kind}:`, e.message);
    }
  }
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
