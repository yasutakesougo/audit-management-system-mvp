/**
 * notify-quality-os — Format Nightly results for Teams notification
 *
 * Usage:
 *   node scripts/ops/notify-quality-os.mjs
 *   node scripts/ops/notify-quality-os.mjs --date 2026-04-24
 */
import fs from 'node:fs';
import path from 'node:path';
import { spawnSync } from 'node:child_process';

const ROOT = process.cwd();
const REPORT_DIR = path.join(ROOT, 'docs', 'nightly-patrol');

const args = process.argv.slice(2);
const dateArgIdx = args.indexOf('--date');
const dateArg = dateArgIdx >= 0 ? args[dateArgIdx + 1] : null;

// Resolve date: explicit arg or today (local time for the file naming convention)
const today = new Date();
const stamp = dateArg || [
  today.getFullYear(),
  String(today.getMonth() + 1).padStart(2, '0'),
  String(today.getDate()).padStart(2, '0'),
].join('-');

const applyResultsPath = path.join(REPORT_DIR, `apply-results-${stamp}.json`);
const classificationPath = path.join(REPORT_DIR, `classification-${stamp}.json`);

if (!fs.existsSync(classificationPath)) {
  console.error(`❌ Classification file not found: ${classificationPath}`);
  console.log('   Note: Ensure nightly-patrol and nightly-classify have run.');
  process.exit(1);
}

const classification = JSON.parse(fs.readFileSync(classificationPath, 'utf8'));
const applyResults = fs.existsSync(applyResultsPath)
  ? JSON.parse(fs.readFileSync(applyResultsPath, 'utf8'))
  : null;

const sections = [];
const statusEmoji = classification.overall === 'stable' ? '✅' : '⚠️';
sections.push(`🚀 **Quality OS / Nightly Patrol Report (${stamp})**`);
sections.push(`**Overall Status:** ${statusEmoji} ${classification.overall.toUpperCase()}`);

if (applyResults && applyResults.results && applyResults.results.length > 0) {
  const created = applyResults.results.filter((r) => r.status === 'created');
  if (created.length > 0) {
    sections.push('### 📋 Created GitHub Issues');
    for (const r of created) {
      sections.push(`- [${r.title}](${r.url})`);
      const codes = [...new Set([...(r.decisionFailCodes || []), ...(r.decisionWarnCodes || [])])];
      if (codes.length > 0) {
        sections.push(`  - **Signals:** \`${codes.join(', ')}\``);
      }
    }
  } else {
    sections.push('ℹ️ No issues were created (dry-run or no actionable items).');
  }
} else {
  // Fallback to classification if no apply results yet
  const actionable = classification.actions.filter((a) => a.action !== 'log-only');
  if (actionable.length > 0) {
    sections.push('### ⚠️ Actionable Findings (Issue creation pending)');
    for (const a of actionable) {
      sections.push(`- **${a.kind}** (Priority: ${a.priority}, Files: ${a.fileCount})`);
    }
    sections.push('Run \`node scripts/ops/nightly-apply.mjs --apply\` to create issues.');
  } else {
    sections.push('✅ No critical issues detected.');
  }
}

sections.push('---');
sections.push(`*Generated by Quality OS Notification Bridge*`);

const message = sections.join('\n\n');

// Call scripts/notify.mjs
const notifyScript = path.join(ROOT, 'scripts', 'notify.mjs');

console.log('--- Teams Notification Message Preview ---');
console.log(message);
console.log('------------------------------------------');

if (process.env.NOTIFY_WEBHOOK_URL) {
  console.log('📡 Sending to Teams...');
  const result = spawnSync('node', [notifyScript, message], { encoding: 'utf8' });
  console.log(result.stdout);
  if (result.status !== 0) {
    console.error(result.stderr);
    process.exit(result.status);
  }
} else {
  console.log('💡 NOTIFY_WEBHOOK_URL not set. Skip actual notification.');
}
