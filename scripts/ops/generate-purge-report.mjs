/**
 * Generate Purge Report — Formats the results for GITHUB_STEP_SUMMARY.
 */
import { readFileSync, existsSync } from 'node:fs';
import { join, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = resolve(__dirname, '..', '..');

const SUMMARY_FILE = join(REPO_ROOT, 'docs', 'nightly-patrol', 'latest-purge-summary.json');
const LOG_FILE = join(REPO_ROOT, 'docs', 'nightly-patrol', 'deletion-log.json');

async function main() {
  if (!existsSync(SUMMARY_FILE)) {
    console.error('❌ Summary file not found.');
    return;
  }

  const summary = JSON.parse(readFileSync(SUMMARY_FILE, 'utf-8'));
  const logs = existsSync(LOG_FILE) ? JSON.parse(readFileSync(LOG_FILE, 'utf-8')) : [];
  
  // Filter logs for the current batch
  const currentBatchLogs = logs.filter(l => l.batchId === summary.batchId);

  let md = `## 💀 SharePoint Zombie Purge Result [${summary.mode}]\n\n`;
  
  md += `| Metric | Value |\n`;
  md += `| --- | --- |\n`;
  md += `| **Batch ID** | \`${summary.batchId}\` |\n`;
  md += `| **Mode** | \`${summary.mode}\` |\n`;
  md += `| **Status** | ${summary.successCount > 0 ? '✅ Executed' : (summary.totalFound > 0 ? '🟡 Dry-run only' : '🟢 No action')} |\n`;
  md += `| **Total Candidates** | ${summary.totalFound} |\n`;
  md += `| **Success** | ${summary.successCount} |\n`;
  md += `| **Failed** | ${summary.failCount} |\n`;
  md += `| **Timestamp** | \`${summary.timestamp}\` |\n\n`;

  if (currentBatchLogs.length > 0) {
    md += `### 📝 Deletion Details\n\n`;
    md += `| List | Internal Name | Display Name | Confidence |\n`;
    md += `| --- | --- | --- | --- |\n`;
    currentBatchLogs.forEach(log => {
      md += `| ${log.listTitle} | \`${log.internalName}\` | ${log.displayName} | **${log.evidence.confidence}** |\n`;
    });
    md += `\n`;
  }

  if (summary.mode === 'DRY-RUN' && summary.totalFound > 0) {
    md += `> [!TIP]\n`;
    md += `> To execute the purge manually, use: \`npm run patrol:purge -- --confirm\`\n\n`;
  }

  md += `*Generated by Nightly Purge Integration*\n`;

  console.log(md);

  if (process.env.GITHUB_STEP_SUMMARY) {
    const fs = await import('node:fs');
    fs.appendFileSync(process.env.GITHUB_STEP_SUMMARY, md);
  }
}

main().catch(err => {
  console.error('❌ Report generation failed:', err);
});
